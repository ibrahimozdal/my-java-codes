package day03scannerincrementdecrement;

public class Decrement01 {

	public static void main(String[] args) {

		// Decrement is decreasing the value of number variable by minus sign "-" and division "/"
		// First way
		
		int num2=1;
		
		num2= num2-3;
		System.out.println(num2); // -2
				
		// second way
		
		num2 -=4; //-6
		num2 /=3; // -2
		System.out.println(num2);
		
		//3 rd way is only to decrease by 1
		
		num2--;
		System.out.println(num2); //-3
		
		
		
		
	}

}
